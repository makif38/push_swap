#include "libft.h"

char	*ft_strdup(const char *s)
{
	char	*d;
	size_t	i;
	size_t	len;

	len = ft_strlen(s);
	d = malloc(len + 1);
	if (!d)
		return (NULL);
	i = 0;
	while (i < len)
	{
		d[i] = s[i];
		i++;
	}
	d[i] = '\0';
	return (d);
}

